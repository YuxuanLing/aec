static inline int trailing_zeros(int v)
{
	int c = 32;  // output: c will count v's trailing zero bits, so if v is 1101000 (base 2), then c will be 3
	if (v) {
		v &= (0 - ((v >> 31) & 0x1));
		if (v) c--;
		if (v & 0x0000FFFF) c -= 16;
		if (v & 0x00FF00FF) c -= 8;
		if (v & 0x0F0F0F0F) c -= 4;
		if (v & 0x33333333) c -= 2;
		if (v & 0x55555555) c -= 1;
	}
	else {
		c = 8 * sizeof(v);
	}

	return c;
}

