void partial_fft(int N, int M, float r[])
{
  const float *s = (float *) c;
  int m = FFT_MAX-N;
  int n = 1;
  int b = N;

  for (int i=0;i<M-2;i++)
  {
    n = n << 1;
    b = b >> 1;       
    for (int j=0;j<n;j+=2)
    {
      for (int k=0;k<b;k+=4)
      {             
        float32x4_t d0,d1,d2,d3,d4,d5,d6,d7;
        float32x4x2_t d43, d02, d15;

        d02 = vld2q_f32(&s[2*(k+m)]);

        d3 = vld1q_f32(&r[2*(k+(j+0)*b)+0]);   
        d0 = vld1q_f32(&r[2*(k+(j+0)*b)+4]);   
        d4 = vld1q_f32(&r[2*(k+(j+1)*b)+0]);
        d5 = vld1q_f32(&r[2*(k+(j+1)*b)+4]);

        d6 = vaddq_f32(d3,d4);
        d3 = vsubq_f32(d3,d4);
        d7 = vaddq_f32(d0,d5);
        d0 = vsubq_f32(d0,d5);
  
        vst1q_f32(&r[2*(k+j*b)+0],d6);
        vst1q_f32(&r[2*(k+j*b)+4],d7);

        d43 = vuzpq_f32 (d3,d0);
       
        d4 = d43.val[0];
        d3 = d43.val[1];
        d0 = d02.val[0];
        d2 = d02.val[1];

        d5 = vmulq_f32(d4,d0);
        d4 = vmulq_f32(d4,d2);
        d1 = vmulq_f32(d3,d2);
        d3 = vmulq_f32(d3,d0);
        d5 = vsubq_f32(d5,d1);
        d3 = vaddq_f32(d3,d4);

        d15.val[0] = d5;
        d15.val[1] = d3;
        vst2q_f32(&r[2*(k+(j+1)*b)],d15);

      }
    }       
    m = m + b;
  }
  for (int i=M-2;i<M;i++)
  {
    n = n << 1;
    b = b >> 1;       
    for (int j=0;j<n;j+=2)
    {
      for (int k=0;k<b;k++)
      {             
        float re_sum = r[2*(k+j*b)+0] + r[2*(k+(1+j)*b)+0];        
        float im_sum = r[2*(k+j*b)+1] + r[2*(k+(1+j)*b)+1];   
        float re_dif = r[2*(k+j*b)+0] - r[2*(k+(1+j)*b)+0];        
        float im_dif = r[2*(k+j*b)+1] - r[2*(k+(1+j)*b)+1];
        r[2*(k+(0+j)*b)+0] = re_sum;
        r[2*(k+(0+j)*b)+1] = im_sum;
        r[2*(k+(1+j)*b)+0] = re_dif*s[2*(k+m)+0] - im_dif*s[2*(k+m)+1];
        r[2*(k+(1+j)*b)+1] = im_dif*s[2*(k+m)+0] + re_dif*s[2*(k+m)+1];
      }
    }       
    m = m + b;
  }
}

/* When this function is built using the arm-linux-androideabi-gcc compiler
   it causes a floating point error (DE24297) which affects the echo canceler. 
   Using the armcc compiler does not have the issue. There wasn't a clean way to 
   distinguish between the compilers since armcc sets most of the gcc macros. So
   when not using armcc, manually #define out this version of complete_fft and 
   use the version below in the #else __ARM_NEON__ */
void complete_fft(const int N, float ri[], float ro[])
{
    // number of butterflies
    int M = _bit_scan_forward(N);
    
    // butterfly
    const float *s = (float *) c;
    int m = FFT_MAX-N;
    int n = 1;
    int b = N;

    for (int i=0;i<M-2;i++)
    {
        n = n << 1;
        b = b >> 1;       
        for (int j=0;j<n;j+=2)
        {
            for (int k=0;k<b;k+=4)
            {             
                float32x4_t d0,d1,d2,d3,d4,d5,d6,d7;
                float32x4x2_t d43, d02, d15;
                
                d02 = vld2q_f32(&s[2*(k+m)]);
                
                d3 = vld1q_f32(&ri[2*(k+(j+0)*b)+0]);   
                d0 = vld1q_f32(&ri[2*(k+(j+0)*b)+4]);   
                d4 = vld1q_f32(&ri[2*(k+(j+1)*b)+0]);
                d5 = vld1q_f32(&ri[2*(k+(j+1)*b)+4]);
                
                d6 = vaddq_f32(d3,d4);
                d3 = vsubq_f32(d3,d4);
                d7 = vaddq_f32(d0,d5);
                d0 = vsubq_f32(d0,d5);
                
                vst1q_f32(&ri[2*(k+j*b)+0],d6);
                vst1q_f32(&ri[2*(k+j*b)+4],d7);
                
                d43 = vuzpq_f32 (d3,d0);
                
                d4 = d43.val[0];
                d3 = d43.val[1];
                d0 = d02.val[0];
                d2 = d02.val[1];
                
                d5 = vmulq_f32(d4,d0);
                d4 = vmulq_f32(d4,d2);
                d1 = vmulq_f32(d3,d2);
                d3 = vmulq_f32(d3,d0);
                d5 = vsubq_f32(d5,d1);
                d3 = vaddq_f32(d3,d4);
                
                d15.val[0] = d5;
                d15.val[1] = d3;
                vst2q_f32(&ri[2*(k+(j+1)*b)],d15);                
            }
        }       
        m = m + b;
    }
    // tail loop
    for (int j=0;j<N>>2;j++)
    {    	
    	float32x4_t a = vld1q_f32(&ri[8*j+0]);
    	float32x4_t b = vld1q_f32(&ri[8*j+4]);
    	float32x4_t add = vaddq_f32(a,b);
    	float32x4_t sub = vsubq_f32(a,b);    	
    	vst1q_f32(&ri[8*j+0],add);
    	vst1_f32(&ri[8*j+4],vget_low_f32(sub));
    	vst1_f32(&ri[8*j+6],vreinterpret_f32_u32(veor_u32(vcreate_u32(0x8000000000000000LLU),vreinterpret_u32_f32(vrev64_f32(vget_high_f32(sub))))));
    }        
    for (int j=0;j<N>>4;j++)
    {      	
    	float32x4x4_t lo,hi;
    	float32x4x4_t a = vld4q_f32 (&ri[32*j+ 0]);
    	float32x4x4_t b = vld4q_f32 (&ri[32*j+16]);    	
       	lo.val[0] = vaddq_f32(a.val[0],a.val[2]);
       	lo.val[1] = vaddq_f32(a.val[1],a.val[3]);
       	lo.val[2] = vsubq_f32(a.val[0],a.val[2]);
       	lo.val[3] = vsubq_f32(a.val[1],a.val[3]);       	
       	hi.val[0] = vaddq_f32(b.val[0],b.val[2]);
       	hi.val[1] = vaddq_f32(b.val[1],b.val[3]);
       	hi.val[2] = vsubq_f32(b.val[0],b.val[2]);
       	hi.val[3] = vsubq_f32(b.val[1],b.val[3]);       	        
    	vst4q_f32 (&ri[32*j+ 0],lo);
    	vst4q_f32 (&ri[32*j+16],hi);              
    }
    // bit reversal
    for(int i=0; i<N; i+=4)
    {
        int idx_0 = (__rbit(i+0)) >> (32 - M);
        int idx_1 = (__rbit(i+1)) >> (32 - M);
        int idx_2 = (__rbit(i+2)) >> (32 - M);
        int idx_3 = (__rbit(i+3)) >> (32 - M);        
        vst1_f32(ro+2*(i+0),vld1_f32(ri+2*idx_0));
        vst1_f32(ro+2*(i+1),vld1_f32(ri+2*idx_1));
        vst1_f32(ro+2*(i+2),vld1_f32(ri+2*idx_2));
        vst1_f32(ro+2*(i+3),vld1_f32(ri+2*idx_3));        
    }    
}

