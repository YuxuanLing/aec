void partial_fft(int N, int M, float r[])
{
    float  *z = (float  *) r;
    int m = FFT_MAX-N;
    int n = 1;
    int b = N;
    for (int i=0;i<M;i++)
    {
        n = n << 1;
        b = b >> 1;       
        for (int j=0;j<n;j+=2)
        {
#ifdef __INTEL_COMPILER
#pragma ivdep
#pragma vector aligned
#endif
            for (int k=0;k<b;k++)
            {      
                float  sum = z[k+j*b] + z[k+(1+j)*b];        
                float  dif = z[k+j*b] - z[k+(1+j)*b];
                z[k+(0+j)*b] = sum;
                z[k+(1+j)*b] = dif * c[k+m];
            }
        }       
        m = m + b;
    }    
}


void complete_fft(const int N, float ri[], float ro[])
{
    float  *zi = (float  *) ri;
    float  *zo = (float  *) ro;
    
    // number of butterflies
    int M = _bit_scan_forward(N);
    
    // butterfly
    partial_fft(N, M, ri);
    
    // bit reversal
    for(int i=0,j=0;i<N-1;i++)
    {
        if(i<j)
        {
            zo[j]=zi[i];
            zo[i]=zi[j];
        }
        else if(i==j)
        {
            zo[i]=zi[i];
            zo[j]=zi[j];
        }
        int k = N >> 1;
        while(k<j+1)
        {
            j = j - k;
            k = k >> 1;
        }
        j = j + k;
    }
    zo[N-1] = zi[N-1];
}

